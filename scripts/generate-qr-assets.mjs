import crypto from "node:crypto";
import fs from "node:fs/promises";
import path from "node:path";
import QRCode from "qrcode";

const rootDir = process.cwd();
const outputDir = path.join(rootDir, "generated", "qr");
const siteUrl = process.env.NEXT_PUBLIC_SITE_URL || "http://localhost:3000";

const attendanceDays = [
  { dayNumber: 1, date: "2026-03-16", label: "Day1" },
  { dayNumber: 2, date: "2026-03-17", label: "Day2" },
  { dayNumber: 3, date: "2026-03-18", label: "Day3" },
  { dayNumber: 4, date: "2026-03-19", label: "Day4" },
  { dayNumber: 5, date: "2026-03-20", label: "Day5" }
];

function createToken() {
  return crypto.randomBytes(18).toString("base64url");
}

function buildCheckUrl(token) {
  return new URL(`/check?token=${token}`, siteUrl).toString();
}

function createSeedSql(rows) {
  const values = rows
    .map(
      (row) => `  (
    ${row.dayNumber},
    '${row.date}',
    '09:00',
    '17:00',
    '${row.token}',
    true
  )`
    )
    .join(",\n");

  return `-- Generated by scripts/generate-qr-assets.mjs
-- Apply after schema.sql

insert into attendance_days (
  day_number,
  attendance_date,
  start_time,
  end_time,
  qr_token,
  is_active
)
values
${values}
on conflict (day_number)
do update
set
  attendance_date = excluded.attendance_date,
  start_time = excluded.start_time,
  end_time = excluded.end_time,
  qr_token = excluded.qr_token,
  is_active = excluded.is_active,
  updated_at = now();
`;
}

function createManifest(rows) {
  return {
    generatedAt: new Date().toISOString(),
    siteUrl,
    rows: rows.map((row) => ({
      dayNumber: row.dayNumber,
      date: row.date,
      label: row.label,
      token: row.token,
      url: row.url,
      pngPath: `generated/qr/${row.fileBase}.png`,
      svgPath: `generated/qr/${row.fileBase}.svg`
    }))
  };
}

async function main() {
  await fs.mkdir(outputDir, { recursive: true });

  const rows = attendanceDays.map((day) => {
    const token = createToken();
    const fileBase = `${day.date}-${day.label.toLowerCase()}`;
    return {
      ...day,
      token,
      fileBase,
      url: buildCheckUrl(token)
    };
  });

  for (const row of rows) {
    const pngPath = path.join(outputDir, `${row.fileBase}.png`);
    const svgPath = path.join(outputDir, `${row.fileBase}.svg`);

    await QRCode.toFile(pngPath, row.url, {
      type: "png",
      width: 960,
      margin: 2,
      color: {
        dark: "#0E4A84",
        light: "#FFFFFFFF"
      }
    });

    const svg = await QRCode.toString(row.url, {
      type: "svg",
      margin: 1,
      color: {
        dark: "#0E4A84",
        light: "#FFFFFFFF"
      }
    });

    await fs.writeFile(svgPath, svg, "utf8");
  }

  const seedSql = createSeedSql(rows);
  const manifest = createManifest(rows);

  await fs.writeFile(path.join(rootDir, "attendance_days.seed.sql"), seedSql, "utf8");
  await fs.writeFile(
    path.join(outputDir, "manifest.json"),
    JSON.stringify(manifest, null, 2),
    "utf8"
  );

  console.log("Generated attendance_days.seed.sql and QR assets:");

  for (const row of rows) {
    console.log(`${row.label} ${row.date}`);
    console.log(`  token: ${row.token}`);
    console.log(`  url:   ${row.url}`);
  }
}

main().catch((error) => {
  console.error(error);
  process.exit(1);
});
